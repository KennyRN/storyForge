// One-off dev script: fetches real .itermcolors palettes from mbadolato/iTerm2-Color-Schemes
// and regenerates src/colorPalettes.ts. Not shipped with the plugin, not run at runtime.
// Usage: node scripts/build-color-palettes.mjs

import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const RAW_BASE = "https://raw.githubusercontent.com/mbadolato/iTerm2-Color-Schemes/master/schemes";

// name -> { light: filename|null, dark: filename }
// Dracula and Nord have no genuine official light counterpart - the dark palette is reused for both.
const PALETTE_SOURCES = {
	Atom: { light: "Atom One Light", dark: "Atom One Dark" },
	Ayu: { light: "Ayu Light", dark: "Ayu" },
	Catppuccin: { light: "Catppuccin Latte", dark: "Catppuccin Mocha" },
	Dracula: { light: null, dark: "Dracula" },
	Everforest: { light: "Everforest Light Med", dark: "Everforest Dark Med" },
	Flexoki: { light: "Flexoki Light", dark: "Flexoki Dark" },
	Gruvbox: { light: "Gruvbox Light", dark: "Gruvbox Dark" },
	Nord: { light: null, dark: "Nord" },
	"Rose Pine": { light: "Rose Pine Dawn", dark: "Rose Pine" },
	Solarized: { light: "iTerm2 Solarized Light", dark: "iTerm2 Solarized Dark" },
};

const CANONICAL_COLOR_ORDER = [
	"Background", "Foreground",
	"Black", "Red", "Green", "Yellow", "Blue", "Magenta", "Cyan", "White",
	"Bright Black", "Bright Red", "Bright Green", "Bright Yellow",
	"Bright Blue", "Bright Magenta", "Bright Cyan", "Bright White",
];

const KEY_MAP = {
	Background: "Background Color",
	Foreground: "Foreground Color",
	Black: "Ansi 0 Color",
	Red: "Ansi 1 Color",
	Green: "Ansi 2 Color",
	Yellow: "Ansi 3 Color",
	Blue: "Ansi 4 Color",
	Magenta: "Ansi 5 Color",
	Cyan: "Ansi 6 Color",
	White: "Ansi 7 Color",
	"Bright Black": "Ansi 8 Color",
	"Bright Red": "Ansi 9 Color",
	"Bright Green": "Ansi 10 Color",
	"Bright Yellow": "Ansi 11 Color",
	"Bright Blue": "Ansi 12 Color",
	"Bright Magenta": "Ansi 13 Color",
	"Bright Cyan": "Ansi 14 Color",
	"Bright White": "Ansi 15 Color",
};

function byte(component) {
	return Math.round(parseFloat(component) * 255)
		.toString(16)
		.padStart(2, "0");
}

function extractColor(xml, key) {
	const keyIndex = xml.indexOf(`<key>${key}</key>`);
	if (keyIndex === -1) throw new Error(`Key not found: ${key}`);
	const dictStart = xml.indexOf("<dict>", keyIndex);
	const dictEnd = xml.indexOf("</dict>", dictStart);
	const block = xml.slice(dictStart, dictEnd);

	const num = (component) => {
		const m = block.match(new RegExp(`<key>${component} Component</key>\\s*<real>([^<]+)</real>`));
		if (!m) throw new Error(`Component not found: ${component} in ${key}`);
		return m[1];
	};

	return `#${byte(num("Red"))}${byte(num("Green"))}${byte(num("Blue"))}`;
}

async function fetchVariant(filename) {
	const url = `${RAW_BASE}/${encodeURIComponent(filename)}.itermcolors`;
	const res = await fetch(url);
	if (!res.ok) throw new Error(`Failed to fetch ${url}: ${res.status}`);
	const xml = await res.text();

	const colors = CANONICAL_COLOR_ORDER.map((name) => ({
		name,
		hex: extractColor(xml, KEY_MAP[name]),
	}));

	return { sourceFile: `${filename}.itermcolors`, colors };
}

async function main() {
	/** @type {Record<string, Record<"light"|"dark", { sourceFile: string, colors: {name:string,hex:string}[] }>>} */
	const result = {};

	for (const [name, { light, dark }] of Object.entries(PALETTE_SOURCES)) {
		console.log(`Fetching ${name}...`);
		const darkVariant = await fetchVariant(dark);
		const lightVariant = light ? await fetchVariant(light) : darkVariant;
		result[name] = { light: lightVariant, dark: darkVariant };
	}

	const presetNames = Object.keys(PALETTE_SOURCES);

	const paletteEntries = presetNames
		.map((name) => {
			const variant = result[name];
			const renderVariant = (v) =>
				`{\n\t\tsourceFile: ${JSON.stringify(v.sourceFile)},\n\t\tcolors: [\n${v.colors
					.map((c) => `\t\t\t{ name: ${JSON.stringify(c.name)}, hex: ${JSON.stringify(c.hex)} },`)
					.join("\n")}\n\t\t],\n\t}`;
			return `\t${JSON.stringify(name)}: {\n\t\tlight: ${renderVariant(variant.light)},\n\t\tdark: ${renderVariant(variant.dark)},\n\t},`;
		})
		.join("\n");

	const presetNameUnion = presetNames.map((n) => JSON.stringify(n)).join(" | ");

	const output = `// AUTO-GENERATED by scripts/build-color-palettes.mjs - do not edit by hand.
// Source: https://github.com/mbadolato/iTerm2-Color-Schemes (raw .itermcolors files)

export type PaletteMode = "light" | "dark";

export type PresetPaletteName = ${presetNameUnion};

export type PaletteName = PresetPaletteName | "Custom";

// "Custom" always listed last - it's the user-defined escape hatch, not a preset.
export const PALETTE_NAMES: PaletteName[] = [${presetNames.map((n) => JSON.stringify(n)).join(", ")}, "Custom"];

export const CUSTOM_PALETTE_SIZE = 5;

export interface PaletteColor {
\tname: string;
\thex: string;
}

export interface PaletteVariant {
\tsourceFile: string;
\tcolors: PaletteColor[];
}

export const CANONICAL_COLOR_ORDER = [
\t"Background", "Foreground",
\t"Black", "Red", "Green", "Yellow", "Blue", "Magenta", "Cyan", "White",
\t"Bright Black", "Bright Red", "Bright Green", "Bright Yellow",
\t"Bright Blue", "Bright Magenta", "Bright Cyan", "Bright White",
] as const;

export const COLOR_PALETTES: Record<PresetPaletteName, Record<PaletteMode, PaletteVariant>> = {
${paletteEntries}
};

/** Resolves the colour list to show in the picker for the plugin's current palette selection. */
export function resolvePaletteColors(
\tpaletteName: PaletteName,
\tmode: PaletteMode,
\tcustomPaletteColors: PaletteColor[],
): PaletteColor[] {
\tif (paletteName === "Custom") return customPaletteColors;
\treturn COLOR_PALETTES[paletteName][mode].colors;
}
`;

	const outPath = path.join(path.dirname(fileURLToPath(import.meta.url)), "..", "src", "colorPalettes.ts");
	writeFileSync(outPath, output, "utf8");
	console.log(`Wrote ${outPath}`);
}

main().catch((err) => {
	console.error(err);
	process.exit(1);
});
